# Declaración y asignación de variables

edad = 20
nombre = "Ana"

# Reasignación de variables
edad = edad + 5 # Ahora edad es 25
nombre = nombre + " María" # Ahora nombre es "Ana María"

# Conversión de Tipos
edad_en_texto = str(edad)
altura = 1.75
altura_en_texto = str(altura)

# Imprimir resultados
print("Nombre Completo: ", nombre)
print("Edad en 5 años: ", edad)
print("Altura en texto:", altura_en_texto)